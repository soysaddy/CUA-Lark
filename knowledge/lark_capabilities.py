LARK_PAGE_SIGNATURES = """
## 飞书页面特征

### IM 主页
- 左侧是会话列表
- 右侧可能是空态、占位内容、最近内容或当前选中会话的预览区域
- 顶部有搜索入口
- 不能仅凭“右侧有内容”就判断为聊天页

### IM 聊天页
- 顶部显示联系人/群名
- 中间是消息气泡列表
- 底部是消息输入框
- 应结合“顶部会话名 + 消息列表 + 底部输入框”等组合信号判断

### 搜索浮层
- 独立搜索层或搜索结果区域
- 聚焦的搜索输入框
- 搜索结果列表或搜索建议区
- 不要依赖“居中弹窗”这一固定布局

### 日历页
- 顶部有日/周/月视图切换
- 主体是时间网格
- 有新建日程按钮

### 云文档主页/列表页
- 常见信号是文档列表、最近文档、目录/筛选区、新建入口
- 更接近“资源列表页”，不应与具体文档编辑页混淆

### 具体文档编辑页
- 常见信号是文档标题、正文编辑区、工具栏/协作信息
- 不要只凭“顶部标题 + 一块内容区”就判断为文档编辑页
"""

